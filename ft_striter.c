#include "libft.h"

void		ft_striter(char *s, void (*f)(char *))
{
	char	*str;
	int		i;

	str = (char *)s;
	if (str && f)
	{
		i = 0;
		while (str[i])
		{
			(*f)(&str[i]);
			i++;
		}
	}
}

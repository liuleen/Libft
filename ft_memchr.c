#include "libft.h"

void		*ft_memchr(const void *s, int c, size_t n)
{
	char	*str;
	size_t	i;

	str = (char *)s;
	i = 0;
	while (i < n)
	{
		if (*str == (char)c)
			return ((void *)str);
		else
			str++;
		i++;
	}
	return (NULL);
}
